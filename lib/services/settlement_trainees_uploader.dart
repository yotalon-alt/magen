/// Service להעלאת נתוני חניכים ליישובים ל-Firestore
/// שימוש: קרא ל-uploadAllSettlementTrainees() פעם אחת
class SettlementTraineesUploader {
  static bool _isUploading = false;

  /// העלאת כל נתוני החניכים ל-Firestore
  /// מחזיר את מספר היישובים שהועלו בהצלחה
  static Future<int> uploadAllSettlementTrainees({
    Function(String message)? onProgress,
  }) async {
    if (_isUploading) {
      onProgress?.call('⚠️ העלאה כבר בתהליך...');
      return 0;
    }

    _isUploading = true;
    int count = 0;

    try {
      onProgress?.call('🚀 מתחיל העלאה...');

      for (final entry in _settlementData.entries) {
        await FirebaseFirestore.instance
            .collection('settlement_trainees')
            .doc(entry.key.trim())
            .set({
              'settlementName': entry.key.trim(),
              'trainees': entry.value,
              'traineeCount': entry.value.length,
              'updatedAt': FieldValue.serverTimestamp(),
            });

        count++;
        onProgress?.call(
          '✅ $count. ${entry.key}: ${entry.value.length} חניכים',
        );
      }

      onProgress?.call('\n🎉 הועלו $count יישובים בהצלחה!');
      return count;
    } catch (e) {
      onProgress?.call('❌ שגיאה: $e');
      rethrow;
    } finally {
      _isUploading = false;
    }
  }

  /// בדיקה אם הנתונים כבר קיימים ב-Firestore
  static Future<bool> checkIfDataExists() async {
    try {
      final snapshot = await FirebaseFirestore.instance
          .collection('settlement_trainees')
          .limit(1)
          .get();
      return snapshot.docs.isNotEmpty;
    } catch (e) {
      return false;
    }
  }

  /// Dialog להעלאת הנתונים
  static Future<void> showUploadDialog(BuildContext context) async {
    final exists = await checkIfDataExists();

    if (!context.mounted) return;

    final shouldProceed = await showDialog<bool>(
      context: context,
      builder: (ctx) => Directionality(
        textDirection: TextDirection.rtl,
        child: AlertDialog(
          title: const Text('העלאת נתוני חניכים'),
          content: Column(
            mainAxisSize: MainAxisSize.min,
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              if (exists) ...[
                const Row(
                  children: [
                    Icon(Icons.warning_amber, color: Colors.orange),
                    SizedBox(width: 8),
                    Expanded(
                      child: Text(
                        'נמצאו נתונים קיימים. ההעלאה תדרוס אותם.',
                        style: TextStyle(color: Colors.orange),
                      ),
                    ),
                  ],
                ),
                const SizedBox(height: 16),
              ],
              Text('סה"כ ${_settlementData.length} יישובים'),
              Text(
                'סה"כ ${_settlementData.values.fold(0, (acc, list) => acc + list.length)} חניכים',
              ),
            ],
          ),
          actions: [
            TextButton(
              onPressed: () => Navigator.pop(ctx, false),
              child: const Text('ביטול'),
            ),
            ElevatedButton(
              onPressed: () => Navigator.pop(ctx, true),
              child: const Text('העלה'),
            ),
          ],
        ),
      ),
    );

    if (shouldProceed != true || !context.mounted) return;

    // הצגת מסך התקדמות
    final messages = <String>[];
    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (ctx) => Directionality(
        textDirection: TextDirection.rtl,
        child: StatefulBuilder(
          builder: (context, setState) {
            // התחל העלאה
            if (messages.isEmpty) {
              uploadAllSettlementTrainees(
                onProgress: (msg) {
                  setState(() => messages.add(msg));
                },
              ).then((_) {
                // סגור את ה-dialog אחרי 2 שניות
                Future.delayed(const Duration(seconds: 2), () {
                  if (ctx.mounted) Navigator.pop(ctx);
                });
              });
            }

            return AlertDialog(
              title: const Text('מעלה נתונים...'),
              content: SizedBox(
                width: 400,
                height: 300,
                child: ListView.builder(
                  itemCount: messages.length,
                  itemBuilder: (_, i) =>
                      Text(messages[i], style: const TextStyle(fontSize: 12)),
                ),
              ),
            );
          },
        ),
      ),
    );
  }

